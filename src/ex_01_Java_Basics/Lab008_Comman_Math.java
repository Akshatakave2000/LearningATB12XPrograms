package ex_01_Java_Basics;

public class Lab008_Comman_Math {
    public static void main(String[] args) {
        System.out.println("2+2");
        System.out.println(2+2);
        System.out.println(2*2);
        System.out.println(2/2);
        System.out.println(2-2);
        System.out.println(3/2);
        System.out.println(10/0); // java.lang.ArithmeticException: / by zero


    }

}
